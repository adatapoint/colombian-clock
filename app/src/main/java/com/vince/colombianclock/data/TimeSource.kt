package com.vince.colombianclock.data

import com.vince.colombianclock.Time
import kotlin.random.Random

fun getAllTimes(): List<Time> {
    val times = mutableListOf<Time>()

    for (hour in 0..23) {
        for (minute in 0..59) {
            times.add(Time(hour, minute))
        }
    }
    return times.toSet().toList()
}

fun generateRandomTimes(count: Int): List<Time> {
    val randomTimes = mutableListOf<Time>()
    repeat(count) {
        val hour = Random.nextInt(0, 23)
        val minute = Random.nextInt(0, 59)
        randomTimes.add(Time(hour, minute))
    }
    return randomTimes
}

